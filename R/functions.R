#' Add 2 to a value
#'
#' @param x integer
#'
#' @return x increased by 2
#' @export
#'
#' @examples stig_add2(4) #6
stig_add2 <- function(x) {
  x <- x + 2
  return(x)
}

#' adding 4 to a value
#'
#' @param x integer
#'
#' @return x increased by 4
#' @export
#'
#' @examples stig_add4(8) #12
stig_add4 <- function(x) {
  x <- x + 4
  return(x)
}
